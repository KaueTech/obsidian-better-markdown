/** 
 * Formatting type for each token.
 */
export enum Format {
    /** Align left mark */
    ALIGN_LEFT = 1,
    /** Align right mark */
    ALIGN_RIGHT,
    /** Align center mark */
    ALIGN_CENTER,
    /** Align justify mark */
    ALIGN_JUSTIFY,
    /** Insertion (underline) */
    INSERTION,
    /** Spoiler */
    SPOILER,
    /** Superscript */
    SUPERSCRIPT,
    /** Subscript */
    SUBSCRIPT,
    /** Highlight */
    HIGHLIGHT,
    /** Highlight color tag */
    COLOR_TAG,
}